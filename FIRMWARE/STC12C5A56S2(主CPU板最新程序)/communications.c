/*********************************************************/
/*函数名:COM1_initial()                                  */
/*函数功能:串口1初始化                                   */
/*入口参数:无                                            */
/*返回值:无                                              */
/*********************************************************/
void COM1_initial(void)
{ 
  SCON = 0x50; 
  S2BRT = RELOAD_COUNT;
  AUXR = 0x11;  
  PCON = 0x00; //SMOD=0波特率不倍增
  EA = 1;      //开总中断
  ES = 1;      
}


