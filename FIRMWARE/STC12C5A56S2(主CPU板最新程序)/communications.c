/*********************************************************/ 
/*函数名:COM1_send_char()                                */
/*函数功能:串口1发送一个字节数据                         */
/*入口参数:数据                                          */    
/*返回值:无                                              */
/*********************************************************/
void COM1_send_char(unsigned char CH) 
{
  ES = 0;
  TI = 0; //发送之前先清除TI
  SBUF = CH;
  while(TI==0);
    TI=0;
  ES=1;
}


