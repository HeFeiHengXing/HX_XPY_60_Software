/*********************************************************/ 
/*函数名:COM1_send_string()                              */
/*函数功能:串口1发送字符串数据                           */
/*入口参数:数据                                          */    
/*返回值:无                                              */
/*********************************************************/
void COM1_send_string(unsigned char *str,int strlen) 
{ 
  int k= 0;
   
  do 
  { 
    WD_IN ^= 1;
    COM1_send_char(*(str + k)); 
    k++; 
  }
  while (k < strlen); 
} 
